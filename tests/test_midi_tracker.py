def test_active_notes_measure():
    tracker = MidiTracker(actual_fps=30, default_split_midi_num=68)
    tracker.active_midi_num_groups =[[67], [60, 67], [60]]
    # part_duration_secs = 0.6666666666666666
    # step durations: [0, 0.6666666666666666, 1.3333333333333333, 2.0]
    expected_measure_events = [(67, 0.0, 0.6666666666666666), (67, 0.6666666666666666, 1.3333333333333333), (60, 0.6666666666666666, 1.3333333333333333), (60, 1.3333333333333333, 2.0)]
    actual_measure_events = tracker.get_active_groups_as_measure_events(None)
    assert actual_measure_events == expected_measure_events

